"""
catalog_service.py — Backend aggregation service for GET /moves, /items, /abilities.

The local Pokémon Showdown data (shared/ps_data/{moves,items,abilities}.json,
generated by scripts/sync_ps_data.py) defines the canonical set of entries —
PokéAPI's own /item list in particular includes ~2100 key items, mail, etc.
that aren't relevant to a team builder, so it is not used to enumerate
entries, only to enrich the ones PS already knows about.

preload() is a one-time background task (started at app startup, same
pattern as PokemonResolverService.load_smogon_data()) that fetches PokéAPI
detail for every PS entry and merges it in. Until it completes, list_*()
methods raise 503; get_*() methods always work via a live per-request
PokéAPI fetch fallback.
"""

